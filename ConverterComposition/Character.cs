namespace ConverterComposition
{
    /// <summary>
    /// An enumeration consists of a set of words for all characters that a real number can contains.
    /// </summary>
    public enum Character : byte
    {
        /// <summary>
        /// '0' symbol.
        /// </summary>
        Zero,

        /// <summary>
        /// '1' symbol.
        /// </summary>
        One,

        /// <summary>
        /// '2' symbol.
        /// </summary>
        Two,

        /// <summary>
        /// '3' symbol.
        /// </summary>
        Three,

        /// <summary>
        /// '4' symbol.
        /// </summary>
        Four,

        /// <summary>
        /// '5' symbol.
        /// </summary>
        Five,

        /// <summary>
        /// '6' symbol.
        /// </summary>
        Six,

        /// <summary>
        /// '7' symbol.
        /// </summary>
        Seven,

        /// <summary>
        /// '8' symbol.
        /// </summary>
        Eight,

        /// <summary>
        /// '9' symbol.
        /// </summary>
        Nine,

        /// <summary>
        /// '+' symbol.
        /// </summary>
        Plus,

        /// <summary>
        /// '-' symbol.
        /// </summary>
        Minus,

        /// <summary>
        /// '.' symbol.
        /// </summary>
        Point,

        /// <summary>
        /// ',' symbol.
        /// </summary>
        Comma,

        /// <summary>
        /// 'E' symbol.
        /// </summary>
        Exponent,

        /// <summary>
        /// <see cref="double.NaN"/> symbol.
        /// </summary>
        NaN,

        /// <summary>
        /// <see cref="double.PositiveInfinity"/> symbol.
        /// </summary>
        PositiveInfinity,

        /// <summary>
        /// <see cref="double.NegativeInfinity"/> symbol.
        /// </summary>
        NegativeInfinity,

        /// <summary>
        /// <see cref="double.Epsilon"/> symbol.
        /// </summary>
        Epsilon,
    }
}
